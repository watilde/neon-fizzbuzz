extern crate neon;

use neon::vm::{Call, Result, JS, Module};
use neon::value::String;

fn hello(call: Call) -> JS<String> {
    let scope = call.scope;
    Ok(String::new(scope, "hello node").unwrap())
}

#[no_mangle]
pub fn node_main(mut module: Module) -> Result<()> {
    module.export("hello", hello)
}
